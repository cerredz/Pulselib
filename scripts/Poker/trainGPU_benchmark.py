"""
DO NOT EDIT THIS FILE.

This file is a grading criteria artifact that measures the steps per second
of the Poker GPU environment. It is used to evaluate the environment's speed
and should remain constant to provide a reliable baseline.
"""

from environments.Poker.Player import PokerQNetwork
from environments.Poker.utils import PokerAgentType, build_actions, get_rotated_agents, load_gpu_agents
import gymnasium as gym
from utils.torch import load_device
import torch
import time

def run_benchmark():
    # Hardcoded configuration
    NUM_PLAYERS = 9
    N_GAMES = 200
    EPISODES = 1
    STARTING_BBS = 100
    POKER_ACTION_SPACE_N = 13
    STATE_SPACE = 40
    ACTION_SPACE = 13
    GAMMA = 0.95
    UPDATE_FREQ = 20
    LEARNING_RATE = 2e-4
    WEIGHT_DECAY = 1e-5
    
    # Environment specific weights
    W1 = 0.5
    W2 = 0.3
    K = 100
    ALPHA = 50

    AGENT_STRINGS = [
        "tight_aggressive", "heuristic_hands", "heuristic_hands", 
        "loose_passive", "tight_aggressive", "random", 
        "loose_passive", "small_ball", "tight_aggressive"
    ]

    device = load_device()
    
    agents, agent_types = load_gpu_agents(
        device, NUM_PLAYERS, AGENT_STRINGS, STARTING_BBS, POKER_ACTION_SPACE_N
    )
    
    # Median exploration vs exploitation setup
    q_net = PokerQNetwork(
        weights_path="", 
        device=device, 
        gamma=GAMMA, 
        update_freq=UPDATE_FREQ, 
        state_dim=STATE_SPACE, 
        action_dim=ACTION_SPACE,
        learning_rate=LEARNING_RATE,
        weight_decay=WEIGHT_DECAY,
        epsilon=0.5,
        epsilon_decay=0.95,
        epsilon_end=0.05
    ).to(device)

    agents.insert(0, q_net)
    agent_types.insert(0, PokerAgentType.QLEARNING)
    
    env = gym.make(
        "Pulse-Poker-GPU-v1",
        device=device,
        agents=agents, 
        n_players=NUM_PLAYERS + 1,
        n_games=N_GAMES, 
        starting_bbs=STARTING_BBS, 
        w1=W1,
        w2=W2,
        K=K,
        alpha=ALPHA
    )
    
    total_steps = 0
    start_time = time.time()
    
    actions = torch.zeros(N_GAMES, dtype=torch.long, device=device)
    
    for episode in range(EPISODES):
        rotated_agents, rotated_types, q_seat, rotations = get_rotated_agents(
            agents, agent_types, episode_idx=episode
        )

        state, info = env.reset(options={
            'rotation': rotations,                    
            'active_players': True,
            'q_agent_seat': q_seat   
        })

        terminated = torch.zeros(N_GAMES, dtype=torch.bool, device=device)
        termination_threshold = torch.tensor(0.8, device=device) 
        idx = 0
        
        while True:
            actions.fill_(0)
            q_mask = (info['seat_idx'] == q_seat)
            build_actions(state, actions, info['seat_idx'], rotated_agents, rotated_types, device)
            
            state_snapshot = state.clone()
            next_state, rewards, dones, truncated, info = env.step(actions)
            
            terminated_before = terminated.clone() 
            terminated |= dones
            active_games = q_mask & ~terminated_before 
            
            # Perform training step
            agents[agent_types.index(PokerAgentType.QLEARNING)].train_step(
                states=state_snapshot[active_games],
                actions=actions[active_games],
                rewards=rewards[active_games],
                next_states=next_state[active_games],
                dones=dones[active_games]
            )

            state = next_state
            
            if idx % 5 == 0:
                if terminated.float().mean() > termination_threshold:
                    break
            idx += 1

        total_steps += (N_GAMES * idx)
        
        # Optional minimal progress tracking 
        if (episode + 1) % 10 == 0:
            current_elapsed = time.time() - start_time
            current_sps = total_steps / current_elapsed if current_elapsed > 0 else 0
            print(f"Episode {episode + 1}/{EPISODES} | Speed: {current_sps:.2f} steps/sec")

    elapsed = time.time() - start_time
    steps_per_sec = total_steps / elapsed if elapsed > 0 else 0
    
    print(f"Benchmark completed.")
    print(f"Total time: {elapsed:.2f}s")
    print(f"Total steps: {total_steps}")
    print(f"Final Steps per second: {steps_per_sec:.2f}")
    
    return steps_per_sec

if __name__ == "__main__":
    run_benchmark()
